package javafx.geometry;

/**
 * A set of values for describing horizontal positioning and alignment.
 * @since JavaFX 2.0
 */
public enum HPos {
	/**
	 * Indicates left horizontal position.
	 */
	LEFT,

	/**
	 * Indicates centered horizontal position.
	 */
	CENTER,

	/**
	 * Indicates right horizontal position.
	 */
	RIGHT,
}
